#include "ft_printf.h"

void	ft_putnbr(int n, size_t *i)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		*i += 11;
		return ;
	}
	else
	{
		if (n < 0)
		{
			n = n * -1;
			write(1, "-", 1);
			*i += 1;
		}
		if (n >= 10)
			ft_putnbr((n / 10), i);
		ft_putchar(((n % 10) + '0'), i);
	}
}
